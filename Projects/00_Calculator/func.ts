export {sum, sub, mult, div}

let sum = (num1:number, num2:number) =>{
    return num1 + num2
}

let sub = (num1:number, num2:number) => {
    return num1 - num2
}

let mult = (num1:number, num2:number) => {
    return num1 * num2
}

let div = (num1:number, num2:number) => {
    return num1 / num2
}